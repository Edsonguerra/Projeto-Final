     Bem-vindo à seção de Ajuda! Aqui você encontrará respostas para as perguntas mais frequentes e orientações sobre como utilizar nosso site de marcações de consultas online.

1. Como criar uma conta?
Para criar uma conta, siga os passos abaixo:

Clique no botão "Registrar" na página inicial.
Preencha o formulário de cadastro com suas informações pessoais, como nome completo, e-mail e número de telefone.
Crie uma senha segura.
Clique em "Enviar" para completar o cadastro.
Você receberá um e-mail de confirmação. Clique no link para ativar sua conta.
2. Como marcar uma consulta?
Para marcar uma consulta, siga os passos abaixo:

Faça login na sua conta.
Navegue até a seção "Marcar Consulta".
Selecione a especialidade médica desejada.
Escolha a data e horário disponíveis que melhor atendam às suas necessidades.
Confirme a marcação da consulta.
Você receberá uma confirmação da consulta por e-mail.
3. Como visualizar minhas consultas agendadas?
Para visualizar suas consultas agendadas:

Faça login na sua conta.
Vá para a seção "Minhas Consultas".
Aqui, você verá uma lista de todas as suas consultas futuras e passadas, incluindo detalhes como data, hora e especialidade.
4. Como cancelar uma consulta?
Para cancelar uma consulta, siga os passos abaixo:

Faça login na sua conta.
Vá para a seção "Minhas Consultas".
Encontre a consulta que deseja cancelar e clique em "Cancelar".
Confirme o cancelamento.
5. Como ver a lista de espera?
Para ver a lista de espera:

Faça login na sua conta.
Vá para a seção "Minhas Consultas".
Clique na consulta específica para ver o número de pessoas na lista de espera.
6. Esqueci minha senha. O que devo fazer?
Se você esqueceu sua senha:

Clique em "Esqueci minha senha" na página de login.
Digite seu e-mail registrado.
Você receberá um e-mail com instruções para redefinir sua senha.
Siga o link e as instruções para criar uma nova senha.
7. Como entrar em contato com o suporte?
Se você precisar de ajuda adicional ou tiver alguma dúvida:

Envie um e-mail para: suporte@hospitalpublico.com
Ligue para: (XX) XXXX-XXXX
Nosso horário de atendimento é de segunda a sexta-feira, das 8h às 18h.
8. Dicas para usar o site de forma eficiente

Certifique-se de manter suas informações pessoais atualizadas.
Verifique seu e-mail regularmente para receber confirmações e lembretes de consultas.
Utilize navegadores atualizados para uma melhor experiência de navegação.
Se possível, cancele consultas com antecedência para liberar vagas para outros pacientes.
Estamos aqui para ajudar!
Nossa equipe de suporte está dedicada a fornecer a melhor experiência possível. Se você tiver qualquer outra dúvida ou precisar de assistência, não hesite em entrar em contato conosco.
     
