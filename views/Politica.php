     Política de Privacidade

Introdução
Nós valorizamos a sua privacidade e estamos comprometidos em proteger as suas informações pessoais. Esta Política de Privacidade explica como coletamos, usamos, compartilhamos e protegemos suas informações ao utilizar nosso portal de marcações de consultas online.

Coleta de Informações
Coletamos informações pessoais que você nos fornece ao utilizar nosso site, tais como:

Nome completo
Data de nascimento
Endereço de e-mail
Número de telefone
Dados das consultas agendadas (especialidade médica, data e hora da consulta)
Também podemos coletar informações de navegação através de cookies e outras tecnologias similares para melhorar sua experiência no site.

Uso das Informações
As informações coletadas são utilizadas para:

Agendar, gerenciar e cancelar consultas
Notificar sobre alterações ou lembretes de consultas
Melhorar nossos serviços e funcionalidades do site
Garantir a segurança do nosso sistema
Compartilhamento de Informações
Suas informações pessoais são confidenciais e não serão vendidas, alugadas ou compartilhadas com terceiros, exceto:

Com os profissionais de saúde e administradores do hospital para fins de agendamento e gestão de consultas
Quando exigido por lei ou para proteger nossos direitos legais
Com provedores de serviços que nos ajudam a operar e melhorar nosso site, desde que cumpram nossas políticas de privacidade e segurança
Proteção das Informações
Implementamos medidas de segurança técnicas e administrativas para proteger suas informações pessoais contra acesso não autorizado, uso indevido, alteração e destruição. No entanto, lembramos que nenhum sistema de segurança é infalível e incentivamos você a proteger suas credenciais de acesso.

Seus Direitos
Você tem o direito de:

Acessar suas informações pessoais que coletamos
Solicitar a correção de informações incorretas ou incompletas
Solicitar a exclusão de suas informações pessoais, sujeito às obrigações legais de retenção de dados
Para exercer seus direitos, entre em contato conosco através dos canais fornecidos no final desta política.

Alterações a Esta Política de Privacidade
Podemos atualizar esta Política de Privacidade periodicamente para refletir mudanças em nossas práticas ou por motivos legais. Notificaremos sobre quaisquer alterações publicando a nova política em nosso site e, se necessário, fornecendo um aviso mais destacado.

